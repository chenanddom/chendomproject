package com.dom.builder;

public class House {
private String top;
private String floor;
private String wall;
public String getTop() {
	return top;
}
public void setTop(String top) {
	this.top = top;
}
public String getFloor() {
	return floor;
}
public void setFloor(String floor) {
	this.floor = floor;
}
public String getWall() {
	return wall;
}
public void setWall(String wall) {
	this.wall = wall;
}

}
